// Precompilar en regex con límites de palabra, ordenado por longitud desc para
// que las frases largas ganen sobre las cortas.
const COMPILED = PHRASES
  .slice()
  .sort((a, b) => b[0].length - a[0].length)
  .map(([en, es]) => {
    const escaped = en.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    return { re: new RegExp(`\\b${escaped}\\b`, 'gi'), es }
  })

// Palabras a limpiar/normalizar antes de traducir.
function preprocess(name) {
  return name
    .toLowerCase()
    .replace(/\bv\.\s*\d+\b/g, '')        // "v. 2", "v. 3"
    .replace(/\(male\)|\(female\)/g, '')   // género
    .replace(/\bpov\b/g, '')               // "back pov"
    .replace(/\s+/g, ' ')
    .trim()
}

/**
 * Traduce un nombre de ejercicio inglés → español (mejor esfuerzo).
 * Devuelve { es, coverage } donde coverage es la fracción de tokens traducidos.
 */
export function translateExerciseName(name) {
  let out = preprocess(name)

  // Extraer el equipo líder para reubicarlo como sufijo (orden natural en español).
  let equipmentSuffix = ''
  for (const { re, es } of EQUIPMENT_LEAD) {
    if (re.test(out)) {
      out = out.replace(re, '').trim()
      equipmentSuffix = es
      break
    }
  }

  for (const { re, es } of COMPILED) {
    out = out.replace(re, es)
  }

  if (equipmentSuffix) out = `${out} ${equipmentSuffix}`.trim()
  // Limpieza final
  out = out
    .replace(/\s+/g, ' ')
    // preposiciones duplicadas por la composición (p. ej. "with stability ball")
    .replace(/\bcon en\b/g, 'en')
    .replace(/\bcon con\b/g, 'con')
    .replace(/\ben en\b/g, 'en')
    .replace(/\bde de\b/g, 'de')
    .replace(/\ba a\b/g, 'a')
    .replace(/\s+([),])/g, '$1')
    .replace(/\(\s*\)/g, '')
    .replace(/^[\s-]+|[\s-]+$/g, '')
    .replace(/\s+/g, ' ')
    .trim()

  // Capitalizar primera letra
  out = out.charAt(0).toUpperCase() + out.slice(1)

  return out
}

// Detecta tokens que quedaron sin traducir (letras ASCII a-z de 2+ chars que no
// son palabras españolas comunes ya introducidas por el léxico).
const deburr = (s) => s.normalize('NFD').replace(/[̀-ͯ]/g, '')

const SPANISH_TOKENS = new Set()
for (const [, es] of PHRASES) for (const w of es.split(/\s+/)) if (w) SPANISH_TOKENS.add(deburr(w))
;['de', 'del', 'la', 'las', 'los', 'el', 'un', 'una', 'en', 'con', 'y', 'a', 'por', 'para']
  .forEach((w) => SPANISH_TOKENS.add(w))

export function untranslatedTokens(spanishName) {
  return deburr(spanishName.toLowerCase())
    .replace(/[^a-z\s]/g, ' ')
    .split(/\s+/)
    .filter((t) => t.length >= 3 && !SPANISH_TOKENS.has(t))
}
